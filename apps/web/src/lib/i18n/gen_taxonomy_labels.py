def titleize(slug: str) -> str:
    return slug.replace("-", " ")


def js_obj(mapping: dict[str, str], indent: int = 2) -> str:
    pad = " " * indent
    lines = ["{"]
    for k in sorted(mapping.keys()):
        v = mapping[k].replace("\\", "\\\\").replace('"', '\\"')
        lines.append(f'{pad}"{k}": "{v}",')
    lines.append("}")
    return "\n".join(lines)


def main() -> None:
    data = json.loads(DUMP.read_text(encoding="utf-8"))
    all_slugs: set[str] = set()
    for v in data.values():
        if isinstance(v, list):
            all_slugs.update(v)
        elif isinstance(v, dict):
            for items in v.values():
                all_slugs.update(items)

    # Only keep ZH entries that exist in taxonomy (plus any curated extras ok)
    zh_filtered = {k: v for k, v in ZH.items() if k in all_slugs}
    covered = len(zh_filtered)
